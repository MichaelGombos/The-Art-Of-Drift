const playSoundsSequentially = (sounds) => {
  let index = 0;

  const playNextSound = () => {
    if (index < sounds.length) {
      sounds[index].volume(dialogueMultiplier)
      sounds[index].once('end', playNextSound);
      sounds[index].play();
      currentPlayingSound = sounds;
      index++;
    }
  }

  playNextSound();
};

